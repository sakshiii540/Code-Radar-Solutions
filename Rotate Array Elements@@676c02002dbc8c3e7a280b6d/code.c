#include<stdio.h>
int main(){
    int n,i,j,k;
    scanf("%d",&n);
    int arr[i];
    for (i=0;i<n;i++){
        scanf("%d",&arr[i]);
    }
    printf("%d\n",arr[i]);
    scanf("%d",&k);
    j=n-k;
    printf("%d\n",j);
    for (i=j;i<n;i++){
        printf("%d\n",arr[i]);
    }

    

    
}